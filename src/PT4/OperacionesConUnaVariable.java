package PT4;

public class OperacionesConUnaVariable {

	public static void main(String[] args) {
		// Declaramos la variable n y mostramos valor
		int n = 1;
		System.out.println("Valor inicial de N = " + n);
		
		//incrementamos n en 77 y mostramos valor
		n = n+77;
		System.out.println("N + 77 = " + n);
		
		//decrementamos n en 3 y mostramos valor
		n = n-3;
		System.out.println("N - 3 = " + n);
		
		//duplicamos su valor y mostramos valor
		n = n*2;
		System.out.println("N * 2 = " + n);

	}

}
